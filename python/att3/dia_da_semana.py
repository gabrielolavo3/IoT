# Exercício 7

dia = int(input("Informe um número de 1 a 7 correspondente a um dia da semana: "))

# Impressão

if dia == 1:
    print ("Esse dia é domingo")
elif dia == 2:
    print ("Esse dia é segunda-feira")
elif dia == 3:
    print ("Esse dia é terça-feira")
elif dia == 4:
    print ("Esse dia é quarta-feira")
elif dia == 5:
    print ("Esse dia é quinta-feira")
elif dia == 6:
    print ("Esse dia é sexta-feira")
elif dia == 7:
    print ("Esse dia é sabádo")